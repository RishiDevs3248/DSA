public class B_05_LeftShift {
    /*
     *  Left Shift
     * 
     * 
     *  6 - 0110
     *  left shift by 1
     *  - > 1100
     * 
     *  left shift = multiply by 2
     */
    public static void main(String[] args) {
        System.out.println(6<<1);
        System.out.println(6<<2);
    } 
}
